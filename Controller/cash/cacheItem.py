

class CacheItem():
    def __init__(self, v, i, c):
        self.value = v
        self.id = i
        self.cacheItem = c

    def get_value(self):
        return self.value

    def get_id(self):
        return self.get_id

    def get_cacheItem(self):
        return self.cacheItem

    def info(self):
        return self.value, self.id, self.cacheItem